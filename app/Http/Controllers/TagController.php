    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {

        //delete tag and remove post conncetion
        $tag = Tag::find($id);
        $tag->delete($id);
        $tag->posts()->detach();
        Session::flash('succss',$tag->name.' removed successfully');
        redirect()->route('tags.index')->withTags($tag);
    }
